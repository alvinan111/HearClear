'use client';

import Link from 'next/link';
import { usePathname, useRouter } from 'next/navigation';
import { createClient } from '@/lib/supabase';

const NAV = [
  { href: '/',          icon: GridIcon,    label: '数据总览' },
  { href: '/users',     icon: UsersIcon,   label: '用户管理' },
  { href: '/orders',    icon: CardIcon,    label: '订单管理' },
  { href: '/feedback',  icon: ChatIcon,    label: '用户反馈' },
  { href: '/push',      icon: BellIcon,    label: '推送通知' },
  { href: '/banners',   icon: ImageIcon,   label: '内容管理' },
  { href: '/config',    icon: GearIcon,    label: '远程配置' },
  { href: '/versions',  icon: BoxIcon,     label: '版本管理' },
  { href: '/analytics', icon: ChartIcon,   label: '数据统计' },
  { href: '/logs',      icon: ListIcon,    label: '操作日志' },
];

export default function DashboardLayout({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();

  async function handleLogout() {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push('/login');
  }

  return (
    <div className="flex h-screen overflow-hidden bg-slate-50">
      {/* ── Sidebar ── */}
      <aside className="w-60 flex-shrink-0 flex flex-col bg-slate-900 text-white">
        {/* Logo */}
        <div className="flex items-center gap-3 px-5 h-16 border-b border-slate-700/60">
          <div className="w-8 h-8 rounded-xl bg-indigo-500 flex items-center justify-center text-lg">👂</div>
          <div>
            <p className="text-sm font-bold leading-none">HearClear</p>
            <p className="text-[10px] text-slate-400 mt-0.5">管理后台 v1.0</p>
          </div>
        </div>

        {/* Nav */}
        <nav className="flex-1 overflow-y-auto py-4 px-3 space-y-0.5">
          {NAV.map(({ href, icon: Icon, label }) => {
            const exact = href === '/';
            const active = exact ? pathname === '/' : pathname.startsWith(href);
            return (
              <Link
                key={href}
                href={href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all ${
                  active
                    ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-900/40'
                    : 'text-slate-400 hover:text-white hover:bg-slate-800'
                }`}
              >
                <Icon className="w-4 h-4 flex-shrink-0" />
                {label}
              </Link>
            );
          })}
        </nav>

        {/* Logout */}
        <div className="p-3 border-t border-slate-700/60">
          <button
            onClick={handleLogout}
            className="flex items-center gap-3 w-full px-3 py-2.5 rounded-xl text-sm font-medium text-slate-400 hover:text-white hover:bg-slate-800 transition-all"
          >
            <LogoutIcon className="w-4 h-4" />
            退出登录
          </button>
        </div>
      </aside>

      {/* ── Main ── */}
      <div className="flex-1 flex flex-col overflow-hidden">
        {/* Top bar */}
        <header className="h-16 flex-shrink-0 flex items-center justify-between px-8 bg-white border-b border-slate-100 shadow-sm">
          <h1 className="text-base font-semibold text-slate-800">
            {NAV.find(n => n.href === '/' ? pathname === '/' : pathname.startsWith(n.href))?.label ?? '管理后台'}
          </h1>
          <div className="flex items-center gap-2">
            <div className="w-8 h-8 rounded-full bg-indigo-100 flex items-center justify-center text-indigo-600 text-sm font-bold">A</div>
            <span className="text-sm text-slate-600 font-medium">Admin</span>
          </div>
        </header>

        {/* Content */}
        <main className="flex-1 overflow-y-auto p-8">
          {children}
        </main>
      </div>
    </div>
  );
}

